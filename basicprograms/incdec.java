package basicprograms;

public class incdec {
    public static void main(String[] args) {
        int a=1;
        int b=2;
        int c=3;

        System.out.println("Prefix Increment ++a: " + (++a)); 
        System.out.println("Postfix Increment b++: " + (b++)); 
        System.out.println("After Postfix Increment c: " + c); 
        System.out.println("Prefix Decrement --a: " + (--a)); 
        System.out.println("Postfix Decrement b--: " + (b--)); 
        System.out.println("After Postfix Decrement c: " + c); 
        System.out.println("Prefix decrement --a: " + (--a)); 
        System.out.println("Prefix Increment --b: " + (--b)); 
    }
}
